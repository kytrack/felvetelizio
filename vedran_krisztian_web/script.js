function ValtTema() {
  var switchElem = document.querySelector(".switch");
  var switchContainer = document.querySelector(".switch-container");

  if (switchContainer.classList.contains("on")) {
      switchContainer.classList.remove("on");
      switchElem.style.transform = "translateX(0)";


      document.body.style.background="white";
      document.body.style.color="black";
      document.querySelectorAll("th").forEach(e=>{
        e.style.backgroundColor="#f2f2f2";
      })
  
  } else {
      switchContainer.classList.add("on");
      switchElem.style.transform = "translateX(100%)";


      document.body.style.background="#3c3c3c";
      document.body.style.color="white";
      document.querySelectorAll("th").forEach(e=>{
        e.style.backgroundColor="#2f2f2f";
      })      
  }
}
